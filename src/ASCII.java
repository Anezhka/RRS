public class ASCII {
    public static void main(String[] args) {

        char dec = 80;
        char hex = 0x50;
        char oct = 0120;
        char bin = 0b01010000;
        char symbol = 'P';

        System.out.println(dec);
        System.out.println(hex);
        System.out.println(oct);
        System.out.println(bin);
        System.out.println(symbol);
    }
}
